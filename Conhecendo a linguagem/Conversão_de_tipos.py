preco = 10
print(preco)

print(' ')

#Primeiro método de conversão Int para float
preco = float(preco)
print(preco)

print(' ')

#Segundo método, utilizando a divisão
preco = 10/3
print(preco)

#Utilizando duas barras na divisão o número permanece inteiro
preco_novo = 10 // 3
print(preco_novo)

print(" ")

#Float para int
preco = int(preco)
print(preco)

